#Profundizando en los sistemas de numeracion

# Sistema decimal
a = 10

#Sistema binario
a = 0b1010

#Sistema octal
a = 0o12

print(f'a: {a}')

#Sistema hexadecimal
a = 0xA
print(f'a hexadecimal: {a}')

a = int('23', 10)
print(f'a decimal: {a}')

#Base binario
a = int('1010', 2)
print(f'a binario: {a}')

# base octal
a = int(f'12', 8)
print(f' a = base octal: {a}')

#Base hexadecimal
a = int('17', 16)
print(f'a= base hexadecimal: {a} ')

#Base 5

a = int('34', 5)
print(f'a = base 5: {a}')